		<h2> Job Seekers</h2>
		<h3> When and Where:</h3>
			<ul>
				<li>Evening</li>
				<li>From home</li>
				<li>Consistent data and power on desktop/laptop</li>
				<li>Possible printer</li>
			</ul>
		<h3> Goal:</h3>
			<p>To apply for a job by submitting an application.</p>
		<h3> Steps:</h3>
			<ul>
				<li>Read job description and determine if the job is a good fit</li>
				<li> If job is not a good fit, consider "Similar Positions" links in right panel</li>
				<li>Possibly save or email job for later application</li>
				<li>Hit "apply now" button to be redirected to employer's site and begin application process</li>
			</ul>

		<h2>Employers</h2>
		<h3> When and Where:</h3>
			<ul>
				<li>During work day (8am-5pm)</li>
				<li>From office</li>
				<li>Consistent data and power on desktop/laptop</li>
				<li>printer available</li>
			</ul>
		<h3> Goal:</h3>
			<p>Have prospective employees sent from dice page to company's job application form.</p>
		<h3> Steps:</h3>
			<ul>
				<li>Post job description and requirements on Dice</li>
				<li>Check that job description page looks acceptable</li>
				<li>Wait, hope for applications</li>
			</ul>
